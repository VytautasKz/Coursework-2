package com.company;


class Calculate {
    double sum(double num1, double num2) {
        return num1 + num2;
    }

    double diff(double num1, double num2) {
        return num1 - num2;
    }

    double times(double num1, double num2) {
        return num1 * num2;
    }

    double mod(double num1, double num2) {
        return num1 % num2;
    }

    double div(double num1, double num2) {
        return num1 / num2;
    }
}